function createSpeakersSection(speakers) {
  if (!speakers || speakers.length === 0) return '';
  return speakers.map(speaker => `
        <div class="speaker">
            <img src="${speaker.image}" class="speaker-image">
            <div class="speaker-info">
                <h4>${speaker.name || ''}</h4>
                ${speaker.titles && speaker.titles.length > 0 ? `
                    <div class="speaker-titles">
                        ${speaker.titles.map(title => `<p>${title}</p>`).join('')}
                    </div>
                ` : ''}
            </div>
        </div>
    `).join('');
}

function getSpeakerBios(speakers) {
  if (!speakers || speakers.length === 0) return '';
  return speakers.map(speaker => {
    if (!speaker.bio) return '';
    return `
            <div class="speaker-bio-details">
                <h5>${speaker.name || ''}</h5>
                <p>${speaker.bio}</p>
            </div>
        `;
  }).join('');
}

function createEventCard(event, uniqueId) {
  // botão “Mais informações”: link direto OU abre modal
  const moreInfoBtn = event.link
    ? `<a href="${event.link}" class="btn btn-outline-primary" target="_blank" rel="noopener noreferrer">Mais informações</a>`
    : `<button type="button" class="btn btn-outline-primary" data-bs-toggle="modal" data-bs-target="#modal-${uniqueId}">Mais informações</button>`;

  return `
    <div class="event-card">
      ${event.photos ? `
        <div class="event-gallery">
          <div id="carousel-${uniqueId}" class="carousel slide" data-bs-ride="carousel">
            <div class="carousel-inner">
              ${(event.photos || []).map((photo, photoIndex) => `
                <div class="carousel-item ${photoIndex === 0 ? 'active' : ''}">
                  <img src="${photo}" class="d-block w-100" alt="Foto do evento">
                </div>
              `).join('')}
            </div>

          </div>
        </div>
      ` : ''}
      <h3 class="event-title">${event.title}</h3>
      <div class="event-date"><i class="bi bi-calendar-check"></i> <span>${event.date}</span></div>
      <div class="event-time"><i class="bi bi-clock"></i> <span>${event.time}</span></div>
      <div class="event-location"><i class="bi bi-geo-alt"></i> <span>${event.location}</span></div>

      ${renderButtons(event.buttons)}

      <div class="event-more">${moreInfoBtn}</div>
    </div>
  `;
}

function createEventModal(event, uniqueId) {
  if (event.link) return ''; // Não cria modal se o evento tiver um link direto
  return `
        <div class="modal fade" id="modal-${uniqueId}" tabindex="-1" aria-labelledby="label-${uniqueId}" aria-hidden="true">
            <div class="modal-dialog modal-lg">
                <div class="modal-content">
                    <div class="modal-header">
                        <h5 class="modal-title" id="label-${uniqueId}">${event.title}</h5>
                        <button type="button" class="btn-close" data-bs-dismiss="modal" aria-label="Close"></button>
                    </div>
                    <div class="modal-body">
                        <div class="event-details">
                            <p><strong>Data:</strong> <span>${event.date}</span></p>
                            <p><strong>Horário:</strong> <span>${event.time}</span></p>
                            <p><strong>Local:</strong> <span>${event.location}</span></p>
                            <p><strong>Descrição:</strong> <span>${event.description}</span></p>
                        </div>
${renderSections(event.sections)}                    
</div>
                    <div class="modal-footer">
                        <button type="button" class="btn btn-secondary" data-bs-dismiss="modal">Fechar</button>
                    </div>
                </div>
            </div>
        </div>
    `;
}

